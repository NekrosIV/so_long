#include "libft.h"

int	ft_print_int(int nbr)
{
	int	len;
	int	n;

	len = 0;
	n = nbr;
	if (n < 0 && n != -2147483648)
	{
		n = n * -1;
		len++;
	}
	if (n == 0)
		len = 1;
	while (n != 0 && n != -2147483648)
	{
		n = n / 10;
		len++;
	}
	if (n == -2147483648)
		len = 11;
	ft_putnbr_fd(nbr, 1);
	return (len);
}
